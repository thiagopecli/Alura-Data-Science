# Calculos com Exponenciação, módulo e divissão inteira:

# Exponenciação(**):
num = 2**3
print(num)

# OU ------------
operador = 5
potencia = 3
print(operador ** potencia)

# Módulo(%): Resto da divisão
num = 7 % 3
print(num)

# OU ------------
dividendo = 11
divisor = 3
print(dividendo % divisor)

# Divisão inteira(//): Retorna o resultado da divisão inteira entre dois numeros
num = 10 // 3
print(num)

# OU ------------
numerador = 10
denominador = 2
print(numerador // denominador)